package com.mobius.ra.core.pojo;

public class DashboardSummary {
	private String traffic_date;
	private int type;
	private String repective_x;
	private int count_y;
	private String insert_time;

	public String getTraffic_date() {
		return traffic_date;
	}

	public void setTraffic_date(String traffic_date) {
		this.traffic_date = traffic_date;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getRepective_x() {
		return repective_x;
	}

	public void setRepective_x(String repective_x) {
		this.repective_x = repective_x;
	}

	public int getCount_y() {
		return count_y;
	}

	public void setCount_y(int count_y) {
		this.count_y = count_y;
	}

	public String getInsert_time() {
		return insert_time;
	}

	public void setInsert_time(String insert_time) {
		this.insert_time = insert_time;
	}
}
